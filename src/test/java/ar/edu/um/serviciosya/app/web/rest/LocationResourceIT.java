    @Test
    @Transactional
    public void createLocation() throws Exception {
        int databaseSizeBeforeCreate = locationRepository.findAll().size();

        // Create the Location
        LocationDTO locationDTO = locationMapper.toDto(location);
        restLocationMockMvc.perform(post("/api/locations")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(locationDTO)))
            .andExpect(status().isCreated());

        // Validate the Location in the database
        List<Location> locationList = locationRepository.findAll();
        assertThat(locationList).hasSize(databaseSizeBeforeCreate + 1);
        Location testLocation = locationList.get(locationList.size() - 1);
        assertThat(testLocation.getStreetAddress()).isEqualTo(DEFAULT_STREET_ADDRESS);
        assertThat(testLocation.getStreetNumber()).isEqualTo(DEFAULT_STREET_NUMBER);
        assertThat(testLocation.getFlatNumber()).isEqualTo(DEFAULT_FLAT_NUMBER);
        assertThat(testLocation.isSafeZone()).isEqualTo(DEFAULT_SAFE_ZONE);
        assertThat(testLocation.getCountry()).isEqualTo(DEFAULT_COUNTRY);
        assertThat(testLocation.getZipCode()).isEqualTo(DEFAULT_ZIP_CODE);
        assertThat(testLocation.getCity()).isEqualTo(DEFAULT_CITY);
        assertThat(testLocation.getProvince()).isEqualTo(DEFAULT_PROVINCE);
        assertThat(testLocation.getDepartment()).isEqualTo(DEFAULT_DEPARTMENT);
    }

