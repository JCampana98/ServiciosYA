    @Test
    @Transactional
    public void updateLocation() throws Exception {
        // Initialize the database
        locationRepository.saveAndFlush(location);

        int databaseSizeBeforeUpdate = locationRepository.findAll().size();

        // Update the location
        Location updatedLocation = locationRepository.findById(location.getId()).get();
        // Disconnect from session so that the updates on updatedLocation are not directly saved in db
        em.detach(updatedLocation);
        updatedLocation
            .streetAddress(UPDATED_STREET_ADDRESS)
            .streetNumber(UPDATED_STREET_NUMBER)
            .flatNumber(UPDATED_FLAT_NUMBER)
            .safeZone(UPDATED_SAFE_ZONE)
            .country(UPDATED_COUNTRY)
            .zipCode(UPDATED_ZIP_CODE)
            .city(UPDATED_CITY)
            .province(UPDATED_PROVINCE)
            .department(UPDATED_DEPARTMENT);
        LocationDTO locationDTO = locationMapper.toDto(updatedLocation);

        restLocationMockMvc.perform(put("/api/locations")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(locationDTO)))
            .andExpect(status().isOk());

        // Validate the Location in the database
        List<Location> locationList = locationRepository.findAll();
        assertThat(locationList).hasSize(databaseSizeBeforeUpdate);
        Location testLocation = locationList.get(locationList.size() - 1);
        assertThat(testLocation.getStreetAddress()).isEqualTo(UPDATED_STREET_ADDRESS);
        assertThat(testLocation.getStreetNumber()).isEqualTo(UPDATED_STREET_NUMBER);
        assertThat(testLocation.getFlatNumber()).isEqualTo(UPDATED_FLAT_NUMBER);
        assertThat(testLocation.isSafeZone()).isEqualTo(UPDATED_SAFE_ZONE);
        assertThat(testLocation.getCountry()).isEqualTo(UPDATED_COUNTRY);
        assertThat(testLocation.getZipCode()).isEqualTo(UPDATED_ZIP_CODE);
        assertThat(testLocation.getCity()).isEqualTo(UPDATED_CITY);
        assertThat(testLocation.getProvince()).isEqualTo(UPDATED_PROVINCE);
        assertThat(testLocation.getDepartment()).isEqualTo(UPDATED_DEPARTMENT);
    }

